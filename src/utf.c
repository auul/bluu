const uint32_t *utf_grapheme_cluster(const uint32_t *src, const uint32_t *end)
{
    if (src == end) {
        return src;
    }

    const uint32_t *prev;
    uint32_t state = 0x0;
    do {
        state = gcb_dfa[state][ucd_gcb(*src)];
        prev = src;
        src += !!state;
    } while (src != prev && src != end);
    return src;
}

size_t utf_grapheme_cluster_len(const uint32_t *src, size_t src_len)
{
    return utf_grapheme_cluster(src, src + src_len) - src;
}

bool utf_grapheme_break(uint32_t *state, uint32_t cp)
{
    return (*state = gcb_dfa[*state][ucd_gcb(cp)]) == 0;
}
